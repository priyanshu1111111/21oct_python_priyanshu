# Q2. How will you remove last object from a list? Suppose list1 is [2, 33, 222, 14, and 25], what is list1 [-1]? 

list = [2,33,222,14,25]

# Remove last object using Slicing
list=list[:-1]

print(list)

# Remove last object using pop function
list1 = ['rajkot','surat','baroda','vapi','valasad']

list1.pop()

print(list1)

# list1[-1] refers to the last element of the list. In this case, it is 25.
